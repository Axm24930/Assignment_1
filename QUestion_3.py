#Tuple For Sisters
Sisters=('Mythili','Mounika','Sharanya')
#Tuple For Brothers
Brothers=('Vishal','Shentan','Aditya','Sai')
#Joining Tupled of Brothers and Sisters  and assigning it to siblings
siblings=Sisters+Brothers
print("Siblings:",siblings)
#How many siblings do you have
print("Number of Siblings:",len(siblings))
#modifying siblings by adding mother and father
siblings= siblings +('Shashikala','Sampath Rao')
#Adding siblings and parents to family members
family_members=siblings
print("Family members :",family_members)
